void ready(char dly)
{
	HAL_Delay(dly);
}
void command(unsigned char cmd)
{
	ready(1);
	GPIOA->ODR = ((cmd >>4) & 0x0F);
	clk();
	GPIOA->ODR = ((cmd >>0) & 0x0F);
  clk();
}
void display(unsigned char cmd)
{
	ready(1);
	GPIOA->ODR = ((cmd >>4) & 0x0F)|(1<<RS);
	clk();
	GPIOA->ODR = ((cmd >>0) & 0x0F)|(1<<RS);
  clk();
}
void clk(void)
{
	GPIOA->ODR |= 1<<EN;
	ready(1);
	GPIOA->ODR &= ~(1<<EN);
	ready(1);
}
